from sympy import symbols, hessian, Eq, solve, pprint

# Переменные и функция
x1, x2, x3 = symbols('x1 x2 x3')
f_x = x1**2 + x2**2 + x3**2 + 0.75*x1*x3 + x2*x3

# Матрица Гессе (вторые частные производные функции)
H = hessian(f_x, (x1, x2, x3))

# Подставим точку экстремума (0, 0, 0)
H_at_extremum = H.subs({x1: 0, x2: 0, x3: 0})

# Применим критерий Сильвестра: для этого найдем определители главных миноров
det_H1 = H_at_extremum[:1, :1].det()  # первый главный минор (1x1)
det_H2 = H_at_extremum[:2, :2].det()  # второй главный минор (2x2)
det_H3 = H_at_extremum.det()          # третий главный минор (3x3)

# Вывод результатов
print("Матрица Гессе при экстремуме:")
pprint(H_at_extremum)  # Используем pprint для форматированного вывода

print("\nОпределитель 1-го минора (1x1):", det_H1)
print("Определитель 2-го минора (2x2):", det_H2)
print("Определитель 3-го минора (3x3):", det_H3)

# Определение типа экстремума на основе критерия Сильвестра
if det_H1 > 0 and det_H2 > 0 and det_H3 > 0:
    print("\nТочка (0, 0, 0) является точкой локального минимума.")
elif det_H1 < 0 or det_H2 < 0 or det_H3 < 0:
    print("\nТочка (0, 0, 0) является точкой седлового типа.")
else:
    print("\nТочка (0, 0, 0) является точкой локального максимума.")
